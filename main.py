@ti.kernel
def create_pixel(i:ti.f16,j:ti.f16,color:vec3):
	zs=int(13-((i**2+j**2)**0.5))
	for ii, jj in ti.ndrange(((i-1)*SCALE, i*SCALE), ((j-1)*SCALE, j*SCALE)):
		for z in range(zs*SCALE):
			scene.set_voxel(vec3(jj, -ii, z), 1, color)	
        
